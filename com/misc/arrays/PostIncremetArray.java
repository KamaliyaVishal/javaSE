package com.misc.arrays;

public class PostIncremetArray
{
	public static void main(String[] args)
	{
		double[] doubleArray = new double[1];
		doubleArray[0] = 10.5; // Initialize the first element of the array
		doubleArray[0]++;
		doubleArray[0] += 1.0; // Increment the first element by 1.0
		// Print the first element of the array
		System.out.println("First element of intArray: " + doubleArray[0]);
	}
}
